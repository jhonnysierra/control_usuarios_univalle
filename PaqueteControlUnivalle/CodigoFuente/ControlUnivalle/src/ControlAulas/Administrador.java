    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        char comilla = '"';
        JFileChooser file = new JFileChooser();
        file.setDialogTitle("Seleccione un archivo");
        file.setAcceptAllFileFilterUsed(true);
        file.setFileFilter(new FileNameExtensionFilter("Archivos CSV", "csv"));
        int seleccion = file.showOpenDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            try {
                ruta = file.getSelectedFile().getAbsolutePath();
                ruta();
                sql2 = "LOAD DATA LOCAL INFILE '" + ruta2 + "' INTO TABLE  `estudiante` FIELDS TERMINATED BY  ';' ENCLOSED BY '" + comilla + "' ESCAPED BY '" + barra + barra + "' LINES TERMINATED BY '" + barra + "r" + barra + "n'";
                pst2 = cn.prepareStatement(sql2);
                rs2 = pst2.executeQuery();
                JOptionPane.showMessageDialog(null, "Se cargó el archivo y se generaron contraseñas correctamente");
                ruta2 = "";
            } catch (SQLException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "No se pudo cargar la base de datos");
            }

            sql = "select codigo from estudiante";
            try {
                pst = cn.prepareStatement(sql);
                rs = pst.executeQuery();
                while (rs.next()) {
                    codigo = rs.getString(1);
                    sql1 = "call generarContrase('" + codigo + "')";
                    pst1 = cn.prepareStatement(sql1);
                    rs1 = pst1.executeQuery();
                }
            } catch (SQLException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "No se pudieron crear las contraseñas");
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

