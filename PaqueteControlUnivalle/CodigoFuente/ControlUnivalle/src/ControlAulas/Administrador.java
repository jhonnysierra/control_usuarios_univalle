    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        JasperReport reporteBitacora;

        try {
            Map parametros = new HashMap();
            parametros.clear();

            reporteBitacora = (JasperReport) JRLoader.loadObject("reportebitacora.jasper");
            JasperPrint jasperPrint = JasperFillManager.fillReport(reporteBitacora, parametros, cc.conexion());
            //-------------------PREVIEW REPORTE--------------------------------
            JasperViewer jviewer = new JasperViewer(jasperPrint, false);
            jviewer.setTitle("ReporteBitacora");
            jviewer.setVisible(true);
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(null, "No se pudo generar el reporte");
            Logger.getLogger(Novedades.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

    public void ruta() {
        char carac;
        carac = (char) 92;
        barra = String.valueOf(carac);
        for (int i = 0; i < ruta.length(); i++) {
            ruta2 = ruta2 + ruta.charAt(i);

            if (ruta.codePointAt(i) == 92) {
                ruta2 = ruta2 + barra;
            }
        }

    }

