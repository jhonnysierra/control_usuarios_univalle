    private void Traerdatos() {

        String serial = String.valueOf(PersistirHojaVida.getMotherboardSN());
        sql = "SELECT  codigo,aula from equipo where serial = '" + serial + "'";
        try {
            pst = cn.prepareStatement(sql);
            rs = pst.executeQuery();
            rs.first();
            result = rs.getString(1);
            aula = rs.getString(2);
        } catch (SQLException ex) {
            Logger.getLogger(AdminInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtCodigo.setText(result);
        aula = admifo.consultarCodigo("aula", "descripcion", aula, "codigo");
        //System.out.print(aula);
        ComboAula.setSelectedItem(aula);

    }

