    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String codigo, aula;

        try {
            admin = new Administrador();
        } catch (IOException ex) {
            Logger.getLogger(RegistrarEquipoHV.class.getName()).log(Level.SEVERE, null, ex);
        }

        int i = JOptionPane.showConfirmDialog(this, "¿Desea Registrar La Hoja de Vida de Este Equipo (PC)?.", "Aviso Importante", JOptionPane.OK_CANCEL_OPTION);

        if (i == 0) {
            codigo = txtCodigo.getText();

            aula = admifo.consultarCodigo("aula", "codigo", (String) ComboAula.getSelectedItem(), "descripcion");
            try {
                PersistirHojaVida miHojaVida = new PersistirHojaVida();
                miHojaVida.insertar(codigo, aula);
                admin.setVisible(true);
                this.dispose();

            } catch (UnknownHostException ex) {
                Logger.getLogger(RegistrarEquipoHV.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SigarException ex) {
                Logger.getLogger(RegistrarEquipoHV.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SocketException ex) {
                Logger.getLogger(RegistrarEquipoHV.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

