    private void output() throws SigarException, UnknownHostException, SocketException {
        SysInfo info = new SysInfo();
        HardDisk hd = new HardDisk();
        ram.setText(String.valueOf(info.totalMemoriaRam()) + " KB");
        freeRam.setText(String.valueOf(info.LibreMemoriaRam()) + " KB");
        busyRam.setText(String.valueOf(info.ocupadaMemoriaRam()) + " KB");
        nompc.setText(info.nombrePc());
        partici.setText(info.particiones().toString());
        sistema.setText(info.nombreSo());
        arch.setText(info.archSO());
        sitemaop.setText(info.versionSO());
        mac.setText("" + info.mac());
        txtPro.setText(info.procesador());
        mod.setText(info.procesadorModel());
        mgz.setText("" + info.procesadorMghz());
        tama.setText(hd.getHardDiskSpace() + " GB");
        fab1.setText(hd.getHardDiskFileSystem());
        txtBoard.setText(String.valueOf(PersistirHojaVida.getMotherboardSN()));

    }

