    public String calcularTmantenimiento() {
        if (cboMantenimiento.getSelectedItem() == "Preventivo") {
            tipoMantenimiento = "001";
        } else {
            tipoMantenimiento = "002";
        }
        return tipoMantenimiento;
    }

    public String calcularEstado() {
        if (cboEstado.getSelectedItem() == "Activo") {
            estado = "1";
        } else {
            estado = "0";
        }
        return estado;
    }

    public String calcularFecha() {
        String Fecha = new SimpleDateFormat("yyyy-MM-dd").format(Calendar.getInstance().getTime());
        return Fecha;
    }

    public String calcularHora() {
        hora = new SimpleDateFormat("HH:mm:ss").format(Calendar.getInstance().getTime());
        return hora;
    }

    public void persistir()  {
        try {
            sql1 = "insert into equipo_novedad values(?,?,?,?,?,?);";
            pst = cn.prepareStatement(sql1);
            pst.setString(1, codPc.getText().trim().toLowerCase().toString());
            pst.setString(2, calcularTmantenimiento());
            pst.setString(3, descripcion.getText().trim());
            pst.setString(4, calcularFecha());
            pst.setString(5, calcularHora());
            pst.setString(6, calcularEstado());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Mantenimiento Registrado.");
        } catch (SQLException ex) {
//           Logger.getLogger(Mantenimiento.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "No se pudo registrar este mantenimiento\nPor favor verifique si este equipo si fue registrado con anterioridad.");
        }

    }

